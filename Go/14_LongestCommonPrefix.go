func longestCommonPrefix(strs []string) string {
	if len(strs) == 0 {
		return ""
	}
	if len(strs) == 1 {
		return strs[0]
	}
	var prefix string
	for i := 0; i < len(strs[0]); i++ {
		prefix = prefix + string(strs[0][i])
		for j := 1; j < len(strs); j++ {
			if i >= len(strs[j]) || strs[j][i] != strs[0][i] {
				return prefix[:len(prefix)-1]
			}
		}
	}
	return prefix
}